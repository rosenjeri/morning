fun main() {
    var fruit="Mangoes"

    when (fruit){
        "Banana" -> println("I like eating Banana")
        "Pineapples"-> println("I like eating Pineapples")
        "Mangoes"-> println("I like eating Mangoes")
        "Oranges"-> println("I like eating Oranges")
        "Apples"-> println("I like eating Apples")

        else -> println("Invalid choice")
    }
}